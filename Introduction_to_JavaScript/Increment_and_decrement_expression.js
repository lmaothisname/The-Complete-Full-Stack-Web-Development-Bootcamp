// The increment operators
var x = 5;
x = x + 1;//6
// or
x++; // 7
// The decrement operators
var y = 5;
y = y - 1;//4
// or
y--; // 3

// we can increase the value more than 1
var z = 5;
z += 2;
z -= 2;

//or the same way
var a = 5;
var b = 3;
a += b; //8

//shorthand operator
/* Addition: +=
  Subtraction: -=
  Multiplication: *=
  Division: /=
*/